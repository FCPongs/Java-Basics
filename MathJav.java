public class MathJav {
    public static void main(String[] args) {
        System.out.println("Java Math class has many methods that allows you to perform mathematical tasks on numbers");

        double num1,num2,num3,num4,num5;
    
        //! Max 
        num1 = Math.max(5,10);
        System.out.println(num1);

        //! Min
        num2 = Math.min(10, 25);
        System.out.println(num2);

        //! Square root
        num3 = Math.sqrt(64);
        System.out.println(num3);

        //! Absolute Value
        num4 = Math.abs(-4.7);
        System.out.println(num4);

        //! Random number
        num5 = Math.random();
    }
}
